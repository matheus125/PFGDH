    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        clientesRisco.setNome_Completo(txtnomeCompleto.getText());
        clientesRisco.setIdade_cliente(Integer.parseInt(txtidade.getText()));
        clientesRisco.setGenero_cliente((String) this.comboGenero.getSelectedItem());
        boolean resultado = controllerClienteRisco.controlSaveClienteRisco(clientesRisco);

        if (resultado == true) {
            JOptionPane.showMessageDialog(this, "Salvo com sucesso!");
            limparCampos();
        } else {
            JOptionPane.showMessageDialog(this, "Erro ao Salvar!");
            limparCampos();
        }

    }//GEN-LAST:event_btnSalvarActionPerformed

